package model;

public class EstatisticaVenda {
	
	private String estado;
	private Double ganho;
	
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
	public Double getGanho() {
		return ganho;
	}
	public void setGanho(Double ganho) {
		this.ganho = ganho;
	}
	
	

}
